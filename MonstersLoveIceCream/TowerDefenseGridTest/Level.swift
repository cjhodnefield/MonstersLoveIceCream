        self.waves = createWaves()
        self.backgroundName = self.name
        self.baseName = "base"
        
        switch name {
        case "park":
            initialMoney = 300
            initialLives = 30
            xAdjust = 0
            yAdjust = 0 //-0.2
        case "park2":
            initialMoney = 200
            initialLives = 30
            xAdjust = 0.4
            yAdjust = 0 //-0.1
        default:
            break
        }
        
        createMapLayout()
        rows = Int32(mapLayout.count)
        cols = Int32(mapLayout[0].count)
        enemyStart = getEnemySpawn()
        baseLocation = getBaseLocation()
        
        adjustForDifficulty()
    }
    
    func adjustForDifficulty() {
        switch difficulty {
        case "Easy":
            break
        case "Medium":
            initialMoney = initialMoney * 3 / 4
        case "Hard":
            initialMoney = initialMoney * 3 / 4
            initialLives = initialLives * 2 / 3
        case "Unreasonable":
            initialMoney = initialMoney / 2
            initialLives = initialLives / 3
        default:
            break
        }
    }
    
    func getEnemySpawn() -> int2 {
        var enemySpawn: int2!
        for row in 0 ..< mapLayout.count {
            for col in 0 ..< mapLayout[0].count {
                if mapLayout[row][col] == 11 {
                    enemySpawn = int2(Int32(col), Int32(flipRow(row)))
                    break
                }
            }
        }
        return enemySpawn
    }
    
    func getBaseLocation() -> int2 {
        var baseLocation: int2!
        for row in 0 ..< mapLayout.count {
            for col in 0 ..< mapLayout[0].count {
                if mapLayout[row][col] == 21 {
                    baseLocation = int2(Int32(col), Int32(flipRow(row)))
                    break
                }
            }
        }
        return baseLocation
    }
    
    func flipRow(row: Int) -> Int {
        return rows - row - 1
    }
    
    func createWaves() -> [Int : [[Int]]] {
        waves = [Int : [[Int]]]()
        
